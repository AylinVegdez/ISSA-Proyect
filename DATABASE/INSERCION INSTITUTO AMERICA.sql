CREATE TABLE PROMEDIOS_FINALES
(
CVE_ALUM INT, FOREIGN KEY(CVE_ALUM) REFERENCES ALUMNO(CVE_ALUM),
GRADO INT NOT NULL,
PROMEDIO_FINAL DECIMAL(4,2) CHECK(PROMEDIO_FINAL>=0 AND PROMEDIO_FINAL<=10)NOT NULL
);





/*CONSULTAS ANIDADAS CON 2 TABLAS Y 4 CONDICIONES*/
--OBTENER LAS CALIFICACIONES DEL 3ER TRIMESTRE DE HISTORIA Y ETICA  DEL ALUMNO CON CLAVE 11 EN EL 3ER TRIMESTRE
SELECT * 
FROM CALIF_ALUMNO 
WHERE CVE_MATERIA IN(
					 SELECT CVE_MATERIA 
					 FROM MATERIA 
					 WHERE NOMBRE_MATERIA='HISTORIA' OR NOMBRE_MATERIA='ETICA') AND CVE_ALUM=11 AND NUMERO_TRIMESTRE=3

-- OBTENER EL NOMBRE Y GRUPO DE LOS ALUMNOS CON PROMEDIO FINAL MAYOR A 9.0 EN 5TO GRADO QUE ESTEN ACTIVOS(1) Y TENGA 10 AÑOS
SELECT ALUM_NOMBRE,CVEGRUPO,ALUM_EDAD FROM ALUMNO WHERE CVE_ALUM IN(
														SELECT CVE_ALUM 
														FROM PROMEDIOS_FINALES
														WHERE PROMEDIO_FINAL>=9.0 AND GRADO=5 )AND ALUM_ESTADO=1 AND ALUM_EDAD=10

/*CONSULTAS ANIDADAS CON 3 TABLAS Y 6 CONDICIONES*/
--OBTENER EL NOMBRE, EDAD, ESTADO DE LOS ALUMNOS QUE ESTEN ACTIVOS Y CONTENGA UN 1 EN SU CURP EN EL SALON 5 O 6
--, Y EL NOMBRE DE PROFESOR SEA ARACELI O PATRICIA
SELECT ALUM_NOMBRE,ALUM_EDAD,ALUM_ESTADO,ALUM_CURP FROM ALUMNO WHERE CVEGRUPO IN(
																	  SELECT CVEGRUPO
																	  FROM GRUPO
																	  WHERE CVEPROF IN(SELECT CVEPROF
																					   FROM PROFESOR
																			 WHERE PROF_NOMBRE LIKE '%PATRICIA%' OR PROF_NOMBRE LIKE '%ARACELI%')
																	  AND SALON=5 OR SALON=6)
													 AND ALUM_ESTADO=1 AND ALUM_CURP LIKE '%1%'

/*OBTENER EL NOMBRE, ESTADO,EDAD,SEXO Y CLAVE DE GRUPO DEL ALUMNO CON ESTADO ACTIVO Y SEXO FEMENINO CON UN PROMEDIO DE 8 
 O MAYOR A 9 Y SU MATERIA SEA CIENCIAS NATURALES O ETICA*/

