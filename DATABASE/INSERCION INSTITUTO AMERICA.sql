										WHERE NOMBRE_MATERIA='CIENCIAS NATURALES' OR NOMBRE_MATERIA='ETICA')
				   AND PROM_MAT_ALUM=8.7 OR PROM_MAT_ALUM>=9.3)

AND ALUM_ESTADO=1 AND ALUM_SEXO='F'
